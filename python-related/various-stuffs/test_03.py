# List

# 리스트는 []괄호를 이용하여 표현, 각 요소는 쉼표로 구분함.
#    0   1   2   3   4 인덱스(색인)번호는 0부터 시작(왼쪽에서 읽는 경우)
a = [10, 20, 30, 40, 50]
#    -5  -4  -3  -2  -1 인덱스가 -1부터 시작함(오른쪽에서 시작하는 경우)

# 리스트 변수 a의 내용 확인하기
print(a)

# 인덱스를 사용한 리스트 변수의 n번째 요소 내용 확인하기
print('a[0] ==', a[0], 'a[2] ==', a[2], 'a[4] ==', a[4])
# -인덱스를 사용한 리스트 변수의 n번째 요소 확인하기
print('a[-1] ==', a[-1], 'a[-2] ==', a[-2], 'a[-5] ==', a[-5])
